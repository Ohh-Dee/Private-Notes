# this will also include a return statement
# return = statement used to end a function and send back a result to the caller

def add(x, y):
    z = x + y
    return z 

def subtract(x, y):
    z = x - y
    return z

def multiply(x, y):
    z = x * y
    return z

def divide(x, y):
    z = x / y
    return z

